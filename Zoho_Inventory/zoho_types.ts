const convertObjToUrl = (obj_1: any, obj_2: any) => {
const obj = {...obj_1, ...obj_2}

      let str = [];
  for (var p in obj)
    if (obj.hasOwnProperty(p)) {
      str.push(encodeURIComponent(p) + "=" + encodeURIComponent(obj[p]));
    }

  return str.join("&");

}


